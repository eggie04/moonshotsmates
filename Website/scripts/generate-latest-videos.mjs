import { existsSync, readFileSync, statSync, writeFileSync } from "node:fs"
import path from "node:path"
import { spawnSync } from "node:child_process"

const MAX_VIDEOS = 5
const SQLITE_QUERY = `SELECT guid, title, posted_at FROM episodes ORDER BY posted_at DESC LIMIT 100;`

function decodeEscapedUnicode(text) {
  return text.replace(/\\u([0-9a-fA-F]{4})/g, (_, hex) => String.fromCharCode(parseInt(hex, 16)))
}

function extractVideoId(guid) {
  const matchers = [/^youtube:([A-Za-z0-9_-]{11})$/, /^rss:yt:video:([A-Za-z0-9_-]{11})$/]
  for (const matcher of matchers) {
    const match = guid.match(matcher)
    if (match) return match[1]
  }
  return null
}

function dbCandidates() {
  const explicit = (process.env.DISCORD_STATE_DB || "").trim()
  const root = path.resolve(process.cwd(), "..")
  const defaults = [
    path.join(root, "Discord Agent", "data", "state.db"),
    path.join(root, "data", "state.db"),
  ]
  return explicit ? [explicit, ...defaults] : defaults
}

function pickDbPath() {
  const valid = dbCandidates().filter((candidate) => existsSync(candidate))
  if (valid.length === 0) {
    throw new Error("No state.db found. Checked DISCORD_STATE_DB and default repo paths.")
  }

  return valid.sort((a, b) => statSync(b).mtimeMs - statSync(a).mtimeMs)[0]
}

function readEpisodeRows(dbPath) {
  const result = spawnSync("sqlite3", ["-separator", "\t", dbPath, SQLITE_QUERY], {
    encoding: "utf8",
  })

  if (result.error) {
    throw result.error
  }
  if (result.status !== 0) {
    throw new Error(result.stderr || "Failed to query sqlite3")
  }

  return result.stdout
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const [guid = "", rawTitle = "", postedAt = ""] = line.split("\t")
      return { guid, title: decodeEscapedUnicode(rawTitle), postedAt }
    })
}

function buildVideos(rows) {
  const seen = new Set()
  const videos = []

  for (const row of rows) {
    const id = extractVideoId(row.guid)
    if (!id || seen.has(id)) continue

    seen.add(id)
    videos.push({
      id,
      title: row.title || "Moonshots Episode",
      url: `https://www.youtube.com/watch?v=${id}`,
      embedUrl: `https://www.youtube.com/embed/${id}`,
      postedAt: row.postedAt,
      guid: row.guid,
    })

    if (videos.length >= MAX_VIDEOS) break
  }

  return videos
}

function renderDataFile(videos, dbPath) {
  const payload = JSON.stringify(videos, null, 2)
  const generatedAt = new Date().toISOString()

  return `// AUTO-GENERATED by scripts/generate-latest-videos.mjs\n// Source DB: ${dbPath}\n// Generated at: ${generatedAt}\n\nexport type MoonshotVideo = {\n  id: string\n  title: string\n  url: string\n  embedUrl: string\n  postedAt: string\n  guid: string\n}\n\nexport const latestMoonshotVideos: MoonshotVideo[] = ${payload}\n\nexport const latestMoonshotVideosGeneratedAt = ${JSON.stringify(generatedAt)}\n`
}

function updateCarouselDropInlineItems(videos) {
  const dropPath = path.resolve(process.cwd(), "Framer/Code/MoonshotsCarouselDrop.tsx")
  const source = readFileSync(dropPath, "utf8")
  const markerStart = "// AUTO-GENERATED START: latestMoonshotVideoItems"
  const markerEnd = "// AUTO-GENERATED END: latestMoonshotVideoItems"
  const startIndex = source.indexOf(markerStart)
  const endIndex = source.indexOf(markerEnd)

  if (startIndex === -1 || endIndex === -1 || endIndex <= startIndex) {
    throw new Error(`Missing carousel inline URL markers in ${dropPath}`)
  }

  const before = source.slice(0, startIndex + markerStart.length)
  const after = source.slice(endIndex)
  const items = videos
    .map((video) =>
      [
        "    {",
        `      id: ${JSON.stringify(video.id)},`,
        `      title: ${JSON.stringify(video.title)},`,
        `      url: ${JSON.stringify(video.url)},`,
        `      embedUrl: ${JSON.stringify(video.embedUrl)},`,
        "    },",
      ].join("\n")
    )
    .join("\n")
  const replacement = `\n  const videos = [\n${items}\n  ]\n  `
  const next = `${before}${replacement}${after}`

  writeFileSync(dropPath, next, "utf8")
}

const dbPath = pickDbPath()
const rows = readEpisodeRows(dbPath)
const videos = buildVideos(rows)

const outPath = path.resolve(process.cwd(), "Framer/Code/MoonshotsLatestVideos_data.ts")
writeFileSync(outPath, renderDataFile(videos, dbPath), "utf8")
updateCarouselDropInlineItems(videos)

console.log(`Generated ${videos.length} latest videos from ${dbPath}`)
console.log(`Wrote ${outPath}`)
